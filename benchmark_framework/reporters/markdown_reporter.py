"""Markdown report generator"""

from pathlib import Path
from .base_reporter import BaseReporter


class MarkdownReporter(BaseReporter):
    """Generate Markdown format reports"""

    @property
    def file_extension(self) -> str:
        return "md"

    def generate(self, summary, output_path: Path):
        """
        Generate Markdown report

        Args:
            summary: BenchmarkSummary object
            output_path: Path to output file
        """
        lines = [
            "# InteractiveBench Benchmark Report",
            f"\n**Generated:** {summary.timestamp}",
            f"\n## Summary",
            f"",
            f"| Metric | Value |",
            f"|--------|-------|",
            f"| **Total Tasks** | {summary.total_tasks} |",
            f"| **Completed** | {summary.completed_tasks} |",
            f"| **Failed** | {summary.failed_tasks} |",
            f"| **Duration** | {summary.total_duration_seconds:.1f}s |",
            f"",
            f"---",
            f"",
            f"## Results by Module",
            f""
        ]

        # Group results by module
        by_module = {}
        for result in summary.results:
            if result.module_name not in by_module:
                by_module[result.module_name] = []
            by_module[result.module_name].append(result)

        # Generate per-module sections
        for module, results in sorted(by_module.items()):
            module_title = module.replace('_', ' ').replace('-', ' ').title()
            lines.append(f"### {module_title}")
            lines.append("")

            # Create table header based on module type
            if module == 'math':
                lines.append("| Model | Accuracy | Correct / Total | Wrong (Done) | Duration |")
                lines.append("|-------|----------|-----------------|--------------|----------|")
            elif module == 'situation_puzzle':
                lines.append("| Model | Accuracy | Turns | Duration |")
                lines.append("|-------|----------|-------|----------|")
            elif module == 'trust_game':
                lines.append("| Model | Avg Score / Round | Cooperation Rate | Duration |")
                lines.append("|-------|-------------------|------------------|----------|")
            else:
                lines.append("| Model | Status | Duration |")
                lines.append("|-------|--------|----------|")

            # Add result rows
            for result in results:
                model = result.model_name
                metrics = result.metrics
                duration = f"{result.duration_seconds:.1f}s"

                if module == 'math':
                    accuracy = f"{metrics.get('accuracy', 0):.2%}"
                    correct = metrics.get('correct_answers', 0)
                    total = metrics.get('total_questions', 0)
                    wrong_done = metrics.get('wrong_but_done', 0)
                    lines.append(f"| {model} | {accuracy} | {correct}/{total} | {wrong_done} | {duration} |")

                elif module == 'situation_puzzle':
                    accuracy = f"{metrics.get('accuracy', 0):.2%}"
                    turns = metrics.get('avg_turns', 'N/A')
                    lines.append(f"| {model} | {accuracy} | {turns} | {duration} |")

                elif module == 'trust_game':
                    avg_score = metrics.get('avg_score_per_round', 0)
                    coop_rate = metrics.get('cooperation_rate', 0)
                    lines.append(f"| {model} | {avg_score:.3f} | {coop_rate:.2%} | {duration} |")

                else:
                    status = result.status
                    lines.append(f"| {model} | {status} | {duration} |")

            lines.append("")

        # Add footer
        lines.extend([
            "---",
            "",
            f"*Report generated by [InteractiveBench](https://github.com/your-repo/InteractiveBench) Unified Testing Framework*",
            ""
        ])

        # Write to file
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write('\n'.join(lines))
